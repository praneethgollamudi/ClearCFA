#!/usr/bin/env node
/**
 * gen-claude-md.js — Auto-maintain CLAUDE.md with current constants + AI-identified gaps
 *
 * Usage:
 *   node gen-claude-md.js                           # dry run — print proposed changes
 *   ANTHROPIC_API_KEY=sk-ant-... node gen-claude-md.js          # + AI gap analysis
 *   ANTHROPIC_API_KEY=sk-ant-... node gen-claude-md.js --write  # write to CLAUDE.md
 *
 * What it does:
 *   1. Extracts key constants from src/app.jsx (pricing, quotas, screens, storage keys)
 *   2. Regenerates the <!-- AUTO_FACTS_START -->...<!-- AUTO_FACTS_END --> block
 *   3. (With API key) Reads git diff + current CLAUDE.md → asks Claude what's missing
 *   4. (With --write) Applies changes to CLAUDE.md
 */

const {execSync} = require('child_process');
const fs = require('fs');
const https = require('https');

const WRITE_MODE = process.argv.includes('--write');
const AUTO_START = '<!-- AUTO_FACTS_START -->';
const AUTO_END   = '<!-- AUTO_FACTS_END -->';

// ── 1. Extract facts from src/app.jsx ────────────────────────────────────────

function extractFacts(src) {
  // Also read edge function for quotas defined there
  const aiProxy = fs.existsSync('supabase/functions/ai-proxy/index.ts')
    ? fs.readFileSync('supabase/functions/ai-proxy/index.ts', 'utf8') : '';
  const combined = src + '\n' + aiProxy;

  const num = (pat) => (combined.match(pat) || [])[1] || '?';
  const str = (pat) => (combined.match(pat) || [])[1] || '?';

  const priceTier1   = num(/const PRICE_TIER1\s*=\s*(\d+)/);
  const priceTier2   = num(/const PRICE_TIER2\s*=\s*(\d+)/);
  const priceRegular = num(/const PRICE_REGULAR\s*=\s*(\d+)/);
  const tier1Slots   = num(/const TIER1_SLOTS\s*=\s*(\d+)/);
  const tier2Slots   = num(/const TIER2_SLOTS\s*=\s*(\d+)/);
  const tier1Taken   = num(/const TIER1_TAKEN\s*=\s*(\d+)/);
  const tier2Taken   = num(/const TIER2_TAKEN\s*=\s*(\d+)/);
  const quotaGen     = num(/FREE_DAILY_LIMIT\s*=\s*(\d+)/);
  // chat limit lives in the edge function; extract from the UpgradeModal string in app.jsx
  const quotaChat    = num(/You've used your (\d+) free AI chat messages/) || num(/FREE_CHAT_DAILY_LIMIT\s*=\s*(\d+)/);
  const referralThr  = num(/REFERRAL_THRESHOLD\s*=\s*(\d+)/);
  const upiId        = str(/PAYMENT_UPI_ID\s*=\s*['"]([^'"]+)['"]/);
  const whatsapp     = str(/PAYMENT_WHATSAPP\s*=\s*['"]([^'"]+)['"]/);

  // Screens — from if(screen===...) blocks
  const screenMatches = [...src.matchAll(/if\s*\(\s*screen\s*===\s*["'](\w+)["']\s*\)/g)];
  const screens = [...new Set(screenMatches.map(m => m[1]))].sort();

  // Storage keys — only cfa_* localStorage keys (not API keys or URLs)
  const storageMatches = [...src.matchAll(/const\s+([\w_]+KEY)\s*=\s*["'](cfa_[^"']+)["']/g)];
  const storageKeys = storageMatches.map(m => ({name: m[1], value: m[2]}));

  // activeTab map — it's a multiline object literal inside a ternary
  const atMatch = src.match(/\(\{(home:"home"[\s\S]*?)\}\[screen\]/);
  const activeTab = atMatch ? '{' + atMatch[1].trim() + '}' : '(not found)';

  // Current app.js cache version
  const html = fs.existsSync('index.html') ? fs.readFileSync('index.html', 'utf8') : '';
  const cacheVer = (html.match(/app\.js\?v=(\d+)/) || [])[1] || '?';

  const tier1Left  = parseInt(tier1Slots) - parseInt(tier1Taken);
  const tier2Left  = parseInt(tier2Slots) - parseInt(tier2Taken);
  const activeTier = parseInt(tier1Taken) < parseInt(tier1Slots) ? 1
                   : parseInt(tier2Taken) < parseInt(tier2Slots) ? 2 : 0;
  const activePrice = activeTier === 1 ? priceTier1 : activeTier === 2 ? priceTier2 : priceRegular;

  return {
    priceTier1, priceTier2, priceRegular,
    tier1Slots, tier2Slots, tier1Taken, tier2Taken, tier1Left, tier2Left,
    activeTier, activePrice,
    quotaGen, quotaChat, referralThr,
    upiId, whatsapp,
    screens, storageKeys, activeTab, cacheVer
  };
}

// ── 2. Build the AUTO_FACTS block ─────────────────────────────────────────────

function buildAutoBlock(f) {
  const tierLabel = f.activeTier === 0 ? 'Regular (all tiers full)'
                  : `Tier ${f.activeTier} — ₹${f.activePrice}/mo`;
  const storageTable = f.storageKeys
    .map(k => `| \`${k.value}\` | \`${k.name}\` |`)
    .join('\n');

  return `${AUTO_START}
<!-- Regenerated by gen-claude-md.js — do not edit this block manually -->

## Quick Reference (auto-maintained)

### Pricing tiers
| Constant | Value | Slots | Taken | Left |
|---|---|---|---|---|
| \`PRICE_TIER1\` | ₹${f.priceTier1} | ${f.tier1Slots} | ${f.tier1Taken} | ${f.tier1Left} |
| \`PRICE_TIER2\` | ₹${f.priceTier2} | ${f.tier2Slots} | ${f.tier2Taken} | ${f.tier2Left} |
| \`PRICE_REGULAR\` | ₹${f.priceRegular} | — | — | — |

**Active:** ${tierLabel}. Update \`TIER1_TAKEN\` / \`TIER2_TAKEN\` manually as subscribers join — all UI prices derive automatically.

### Free quotas
| Type | Limit | quota_date format |
|---|---|---|
| AI questions | ${f.quotaGen}/day | \`YYYY-MM-DD\` |
| AI chat | ${f.quotaChat}/day | \`chat-YYYY-MM-DD\` |

Referral threshold: **${f.referralThr} paid subscribers** = 1 free Pro month.

### Payment
- UPI: \`${f.upiId}\`
- WhatsApp: \`${f.whatsapp}\`
- Manual grant: insert row into Supabase \`subscriptions\` table (active=true, valid_until=+30d)

### Screens (${f.screens.length} total)
\`${f.screens.join('`, `')}\`

### activeTab map (current)
\`\`\`
${f.activeTab}
\`\`\`

### Storage keys
| localStorage value | Constant |
|---|---|
${storageTable}

### Build
Cache version: \`app.js?v=${f.cacheVer}\` (increment by 100000 before each commit)
${AUTO_END}`;
}

// ── 3. AI gap analysis ────────────────────────────────────────────────────────

async function getAIAdditions(apiKey, claudeMd, facts) {
  // Get recent commits that changed src/ or supabase/ (skip bot + skip ci)
  let diff = '';
  try {
    const log = execSync(
      `git log --oneline -20 --no-merges 2>/dev/null`,
      {encoding:'utf8'}
    ).trim().split('\n')
     .filter(l => !l.includes('[skip ci]') && !l.includes('github-actions'))
     .join('\n');

    // Get the actual diff for these commits vs the base
    diff = execSync(
      `git diff HEAD~5 HEAD -- src/app.jsx supabase/ 2>/dev/null || git diff HEAD~3 HEAD -- src/app.jsx supabase/ 2>/dev/null || true`,
      {encoding:'utf8'}
    ).slice(0, 6000); // cap size

    diff = `Recent commits:\n${log}\n\nCode diff (truncated):\n${diff}`;
  } catch {}

  if (!diff.trim()) return null;

  const prompt = `You are helping maintain CLAUDE.md for ClearCFA, an AI-powered CFA exam prep app.
CLAUDE.md is a reference document for Claude Code sessions — it documents architecture, patterns, business logic, and gotchas.

Current CLAUDE.md:
<claude_md>
${claudeMd.slice(0, 8000)}
</claude_md>

Recent changes to the codebase:
<diff>
${diff}
</diff>

Identify any changes in the diff that should be reflected in CLAUDE.md but aren't documented yet.
Focus on:
- New feature systems (new state, new functions, new screens, new edge functions)
- Changed behaviour of already-documented systems (e.g. referral flow changed, new quota limit)
- New constants or invariants future sessions would need to know
- New gotchas or constraints

Rules:
- Only suggest ADDITIONS — never suggest removing or rewriting existing documentation
- Skip internal refactors, minor tweaks, CI changes, or things already well-documented
- Keep each addition concise (2–6 lines of markdown)
- Skip anything already covered in the CLAUDE.md shown above

Respond with ONLY valid JSON — no markdown fences, no commentary:
{
  "no_changes_needed": true,
  "additions": []
}
OR:
{
  "no_changes_needed": false,
  "additions": [
    {
      "section": "## Common Gotchas",
      "text": "- **New gotcha**: explanation here."
    }
  ]
}

Each addition must specify an existing CLAUDE.md section heading to append to (use exact heading text),
or use "## Appendix" to add a new section at the end.`;

  const body = JSON.stringify({
    model: 'claude-haiku-4-5-20251001',
    max_tokens: 1200,
    messages: [{role:'user', content: prompt}]
  });

  return new Promise((resolve) => {
    const options = {
      hostname: 'api.anthropic.com',
      path: '/v1/messages',
      method: 'POST',
      headers: {
        'x-api-key': apiKey,
        'anthropic-version': '2023-06-01',
        'content-type': 'application/json',
        'content-length': Buffer.byteLength(body)
      }
    };

    const proxy = process.env.HTTPS_PROXY || process.env.https_proxy;
    if (proxy) {
      try {
        const {URL} = require('url');
        const u = new URL(proxy);
        options.hostname = u.hostname;
        options.port = u.port || 443;
        options.path = `https://api.anthropic.com/v1/messages`;
        options.headers['host'] = 'api.anthropic.com';
      } catch {}
    }

    const req = https.request(options, res => {
      let data = '';
      res.on('data', c => data += c);
      res.on('end', () => {
        try {
          const parsed = JSON.parse(data);
          if (parsed.error) { console.error('AI error:', parsed.error.message); return resolve(null); }
          let text = (parsed.content?.[0]?.text || '').replace(/```json\n?|```/g,'').trim();
          const jsonM = text.match(/\{[\s\S]*\}/);
          if (jsonM) text = jsonM[0];
          const result = JSON.parse(text);
          resolve(result);
        } catch(e) {
          console.error('AI parse error:', e.message);
          resolve(null);
        }
      });
    });
    req.on('error', e => { console.error('AI request error:', e.message); resolve(null); });
    req.write(body);
    req.end();
  });
}

// ── 4. Apply additions to CLAUDE.md ──────────────────────────────────────────

function applyAdditions(claudeMd, additions) {
  let result = claudeMd;
  for (const add of additions) {
    if (!add.section || !add.text) continue;
    if (add.section === '## Appendix') {
      result = result.trimEnd() + '\n\n' + add.text + '\n';
    } else {
      // Find the section heading and append after its content (before the next ##)
      const headingEsc = add.section.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
      const re = new RegExp(`(${headingEsc}[\\s\\S]*?)(?=\\n##|$)`, 'm');
      const match = result.match(re);
      if (match) {
        const insertion = '\n\n' + add.text.trim();
        result = result.replace(re, match[1].trimEnd() + insertion + '\n');
      } else {
        // Section not found — append at end
        console.warn(`  ⚠️  Section "${add.section}" not found — appending at end`);
        result = result.trimEnd() + '\n\n' + add.text + '\n';
      }
    }
  }
  return result;
}

// ── Main ──────────────────────────────────────────────────────────────────────

async function main() {
  const src = fs.readFileSync('src/app.jsx', 'utf8');
  const facts = extractFacts(src);
  const autoBlock = buildAutoBlock(facts);

  let claudeMd = fs.existsSync('CLAUDE.md') ? fs.readFileSync('CLAUDE.md', 'utf8') : '';
  let updatedMd = claudeMd;

  // Replace or append the AUTO_FACTS block
  if (updatedMd.includes(AUTO_START) && updatedMd.includes(AUTO_END)) {
    const re = new RegExp(`${escRe(AUTO_START)}[\\s\\S]*?${escRe(AUTO_END)}`);
    updatedMd = updatedMd.replace(re, autoBlock);
  } else {
    updatedMd = updatedMd.trimEnd() + '\n\n' + autoBlock + '\n';
  }

  // AI gap analysis
  const apiKey = process.env.ANTHROPIC_API_KEY;
  let aiResult = null;
  if (apiKey) {
    console.log('Calling Claude Haiku for gap analysis...');
    aiResult = await getAIAdditions(apiKey, claudeMd, facts);
    if (aiResult && !aiResult.no_changes_needed && Array.isArray(aiResult.additions) && aiResult.additions.length > 0) {
      console.log(`AI identified ${aiResult.additions.length} addition(s).`);
      // Safety: cap each addition at 1000 chars
      const safe = aiResult.additions
        .filter(a => a.section && a.text && a.text.length < 1000)
        .slice(0, 5);
      updatedMd = applyAdditions(updatedMd, safe);
    } else {
      console.log('AI: no documentation gaps found.');
    }
  } else {
    console.log('ℹ️  No ANTHROPIC_API_KEY — skipping AI gap analysis.');
  }

  const changed = updatedMd !== claudeMd;
  console.log(changed ? '\n✅ Changes detected.' : '\nℹ️  No changes to CLAUDE.md.');

  if (!WRITE_MODE) {
    if (changed) {
      console.log('\n── Proposed CLAUDE.md diff (AUTO block + AI additions) ──');
      console.log(autoBlock.slice(0, 800) + (autoBlock.length > 800 ? '\n...(truncated)' : ''));
      console.log('\nRun with --write to apply.');
    }
    return;
  }

  if (changed) {
    fs.writeFileSync('CLAUDE.md', updatedMd);
    console.log('✅ CLAUDE.md written.');
  }
}

function escRe(s) { return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&'); }

main().catch(e => { console.error(e); process.exit(1); });
